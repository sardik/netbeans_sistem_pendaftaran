    private void tblsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblsaveActionPerformed
        // TODO add your handling code here:
        String vid       = tfidjurusan.getText();
        String vjurusan  = tfjurusan.getText();
        if (tfidjurusan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Data Masih Kosong",
                "PERHATIAN", JOptionPane.WARNING_MESSAGE);
        }
        else {
            try {
                java.sql.Statement stat=conec.createStatement();
                ResultSet data=stat.executeQuery("SELECT * FROM tbljurusan WHERE ID_Jurusan='"+tfidjurusan.getText()+"'");
                if (data.next()){
                    JOptionPane.showMessageDialog(null,"ID Jurusan sudah dimasukkan");
                    tfidjurusan.requestFocus();
                }
                else {
                    String sql = "INSERT INTO tbljurusan VALUES('"+vid+"','"+vjurusan+"')";
                    stat.executeUpdate(sql);
                    tfidjurusan.setText("");
                    tfjurusan.setText("");
                    tfidjurusan.requestFocus();
                }
                nomoran();
                stat.close();
            }
            catch (Exception exc){
                System.err.println("Terjadi Kesalahan :"+exc);
            }
        }
    }//GEN-LAST:event_tblsaveActionPerformed

